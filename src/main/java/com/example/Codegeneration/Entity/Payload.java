package com.example.Codegeneration.Entity;

public class Payload {

    private int originator_id;
    private String originator_name;
    private String tech_stack;

    public int getOriginator_id() {
        return originator_id;
    }

    public void setOriginator_id(int originator_id) {
        this.originator_id = originator_id;
    }

    public String getOriginator_name() {
        return originator_name;
    }

    public void setOriginator_name(String originator_name) {
        this.originator_name = originator_name;
    }

    public String getTech_stack() {
        return tech_stack;
    }

    public void setTech_stack(String tech_stack) {
        this.tech_stack = tech_stack;
    }
}
